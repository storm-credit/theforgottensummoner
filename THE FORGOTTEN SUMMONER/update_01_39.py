def process_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    new_content = content
    changes_made = False
    
    for pattern, rep in replacements.items():
        if re.search(pattern, new_content):
            new_content = re.sub(pattern, rep, new_content)
            changes_made = True
            
    for pattern, replacement in link_mappings.items():
        if re.search(pattern, new_content):
            def repl(m):
                label = m.group(1) if m.group(1) else m.group(0).strip("[]").split("|")[-1]
                target_path = replacement.split("|")[0].replace("[[", "")
                if "01-8. 세력 아카이브" in m.group(0):
                    return f"[[{target_path}|{label}]]"
                return f"[[{target_path}|{label}]]"
            
            new_content = re.sub(pattern, repl, new_content)
            changes_made = True
            
    if changes_made:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        print(f"Updated: {os.path.basename(filepath)}")

